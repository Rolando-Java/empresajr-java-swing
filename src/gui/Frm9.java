    private void cargarTabla(String nombre_empleado,String codigo_estilo){
        Connection cn=null;
        Statement st=null;
        try{
            select_table=false;
                    
            cn=Conection.getConnection();
            st=cn.createStatement();
            
            int codigo_ficha=0,codigo_empleado=0;
            
            ResultSet rs2=st.executeQuery("SELECT FT.COD_FICHA FROM DBO.FICHA_TECNICA FT WHERE FT.COD_ESTILO='"+codigo_estilo+"'");
            
            if(rs2.next()){
                codigo_ficha=rs2.getInt(1);
            }
            
            ResultSet rs3=st.executeQuery("SELECT E.COD_EMP FROM DBO.EMPLEADO E WHERE E.APELLIDO+', '+E.NOMBRE='"+nombre_empleado+"'");
            
            if(rs3.next()){
                codigo_empleado=rs3.getInt(1);
            }
            
            ResultSet rs=st.executeQuery("SELECT FT.COD_ESTILO AS \"CODIGO DE ESTILO\",E.APELLIDO+', '+E.NOMBRE AS \"EMPLEADO\",A.PRENDAS_BIEN AS \"PRENDAS BIEN ELABORADAS\",A.PRENDAS_MAL AS \"PRENDAS MAL ELABORADAS\",CONVERT(VARCHAR,A.FECHA_INICIO,105) AS \"FECHA DE AVANCE\" FROM DBO.EMPLEADO E,DBO.AVANCE A,DBO.FICHA_TECNICA FT WHERE E.COD_EMP=A.COD_EMP AND FT.COD_FICHA=A.COD_FICHA AND FT.COD_FICHA='"+Integer.toString(codigo_ficha)+"' AND E.COD_EMP='"+Integer.toString(codigo_empleado)+"'");
            
            modelo.setColumnCount(0);
            modelo.setRowCount(0);
            
            ResultSetMetaData md=rs.getMetaData();
            int num_column=md.getColumnCount();
            
            for(int i=1;i<=num_column;i++){
                modelo.addColumn(md.getColumnLabel(i));
            }
            
            while(rs.next()){
                Object[] fila=new Object[num_column];
                for(int i=0;i<num_column;i++){
                    fila[i]=rs.getObject(i+1);
                }
                modelo.addRow(fila);
            }
                    
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    
    
