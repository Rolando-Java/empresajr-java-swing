    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        Connection cn=null;
        Statement st=null;
        try{
            cn=Conection.getConnection();
            st=cn.createStatement();
            
            if(select_table){
                
                int codigo_ficha=0,codigo_empleado=0,prendas_bien=0,prendas_mal=0;
            
                ResultSet rs1=st.executeQuery("SELECT FT.COD_FICHA FROM DBO.FICHA_TECNICA FT WHERE FT.COD_ESTILO='"+this.codigo_estilo2+"'");

                if(rs1.next()){
                    codigo_ficha=rs1.getInt(1);
                }

                ResultSet rs2=st.executeQuery("SELECT E.COD_EMP FROM DBO.EMPLEADO E WHERE E.APELLIDO+', '+E.NOMBRE='"+this.nombre_empleado+"'");

                if(rs2.next()){
                    codigo_empleado=rs2.getInt(1);
                }
                
                prendas_bien=(int)spin1.getValue();
                prendas_mal=(int)spin2.getValue();
                
                st.executeUpdate("UPDATE DBO.AVANCE SET PRENDAS_BIEN='"+Integer.toString(prendas_bien)+"',PRENDAS_MAL='"+Integer.toString(prendas_mal)+"' WHERE CONVERT(VARCHAR,FECHA_INICIO,105)=CONVERT(VARCHAR,'"+this.fecha_inicio+"',105)");
                
                cargarTabla(this.nombre_empleado, this.codigo_estilo2);
                this.fecha_inicio=null;
                this.codigo_estilo2=null;
                this.nombre_empleado=null;
                
                JOptionPane.showMessageDialog(null,"se modificó correctamente!!");
            }else{
                JOptionPane.showMessageDialog(null,"seleccione una fila de la tabla!!");
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btn_modificarActionPerformed

    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int numero_fila=tabla.getSelectedRow();
        if(numero_fila>=0){
            select_table=true;
            this.codigo_estilo2=tabla.getValueAt(numero_fila, 0).toString().toUpperCase().trim();
            this.nombre_empleado=tabla.getValueAt(numero_fila, 1).toString().toUpperCase().trim();
            spin1.setValue(Integer.parseInt(tabla.getValueAt(numero_fila, 2).toString()));
            spin2.setValue(Integer.parseInt(tabla.getValueAt(numero_fila, 3).toString()));
            this.fecha_inicio=tabla.getValueAt(numero_fila, 4).toString().toUpperCase().trim();
        }
    }//GEN-LAST:event_tablaMouseClicked

