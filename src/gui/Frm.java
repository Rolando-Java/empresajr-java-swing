    private void btn_sesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sesionActionPerformed
        Connection cn=null;
        Statement st=null;
        try{
            boolean existe_sesion=false;
            String usuario=txt_usuario.getText();
            String contrasena=txt_contrasena.getText();
            
            cn=Conection.getConnection();
            st=cn.createStatement();
            
            ResultSet rs=st.executeQuery("select nombre from dbo.usuario where nombre='"+usuario+"' and password='"+contrasena+"';");//sentencia para validadrla existencia de la sesion, mediante un uusarioo u contrase침a que  sean unicos
            if(rs.next()){
                existe_sesion=true;
            }
            
            if(existe_sesion){
                Frm8 objF=new Frm8();
                objF.setVisible(true);
                objF.setLocationRelativeTo(null);
                objF.setResizable(false);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "No sepudo inciciar sesi칩n!!");
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btn_sesionActionPerformed

